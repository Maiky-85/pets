    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        String email = this.campoEmail.getText();
        String senha = new String(campoSenha.getPassword());

        try{
            Conta login = new Conta();
            login.logarConta(email, senha);
            InicialV2 principal = new InicialV2();
            principal.setVisible(true);
            this.dispose(); 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Não entrou", JOptionPane.QUESTION_MESSAGE); 
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed

